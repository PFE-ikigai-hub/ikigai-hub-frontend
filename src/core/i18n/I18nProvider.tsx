const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const stored = localStorage.getItem("ikigai-lang") as Language | null;
    return stored === "FR" || stored === "EN" || stored === "AR" ? stored : "FR";
  });

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem("ikigai-lang", lang);
  }, []);

  useEffect(() => {
    if (language === "AR") {
      document.documentElement.dir = "rtl";
      document.documentElement.lang = "ar";
    } else {
      document.documentElement.dir = "ltr";
      document.documentElement.lang = language.toLowerCase();
    }
  }, [language]);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>): string => {
      const lang = language.toLowerCase();
      const rawTemplate = translations[lang]?.[key];
      const template = rawTemplate ?? key.split(".").pop()?.replace(/_/g, " ") ?? key;
      if (!vars) return template;
      return Object.entries(vars).reduce((acc, [name, value]) => {
        const stringValue = String(value);
        return acc.replaceAll(`{${name}}`, stringValue);
      }, template);
    },
    [language]
  );

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useI18n() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
